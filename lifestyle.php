             <a href="Immune System 1.html"><h1>Immune System</h1> </a>
            <p>The cells that are part of this defense system include white blood cells, also called leukocytes (pronounced: LOO-kuh-sytes). They come in two basic types (more on these below), which combine to seek out and destroy the organisms or substances that cause disease.
Leukocytes are produced and stored in many locations throughout the body, including the thymus, spleen, and bone marrow. For this reason, they are called the lymphoid (pronounced: LIM-foyd) organs. There are also clumps of lymphoid tissue throughout the body, primarily in the form of lymph nodes, that house the leukocytes.
The leukocytes circulate through the body between the organs and nodes by means of the lymphatic (pronounced: lim-FAT-ik) vessels. (You can think of the lymphatic vessels as a type of highway between the rest stops that are the lymphoid organs and lymph nodes.) Leukocytes can also circulate through the blood vessels. In this way, the immune system works in a coordinated manner to monitor the body for germs or substances that might cause problems.</p>
